package com.demo.datatapes;

public class LoopsDemo {

	public static void main(String[] args) {
		
		
		//for while do-while
		
		
		for(int i =1 ;i <=10 ; i++) {
			//System.out.println(i);
		}
		
		int j=11;
		while(j<=10) {
			System.out.println(j);
			j++;
		}
		int k =11;
		do {
			System.out.println(k);
			k++;
		}while(k<=10);
			
		
	}

}
